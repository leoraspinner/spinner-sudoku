package spinner.sudoku;

public class Main {
    public static void main(String[] args) {
        // Example partially filled Sudoku board
        int[][] initialBoard = {
                {5, 3, 0, 0, 7, 0, 0, 0, 0},
                {6, 0, 0, 1, 9, 5, 0, 0, 0},
                {0, 9, 8, 0, 0, 0, 0, 6, 0},
                {8, 0, 0, 0, 6, 0, 0, 0, 3},
                {4, 0, 0, 8, 0, 3, 0, 0, 1},
                {7, 0, 0, 0, 2, 0, 0, 0, 6},
                {0, 6, 0, 0, 0, 0, 2, 8, 0},
                {0, 0, 0, 4, 1, 9, 0, 0, 5},
                {0, 0, 0, 0, 8, 0, 0, 7, 9}
        };

        // Create a Sudoku object with the initial board
        Sudoku sudoku = new Sudoku(initialBoard);

        // Launch the GUI and pass the Sudoku object to it
        new SudokuGui(sudoku);
    }
}

